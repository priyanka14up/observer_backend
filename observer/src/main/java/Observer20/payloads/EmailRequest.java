package Observer20.payloads;

public class EmailRequest {

	private String Email;
	private long mobnum;

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public long getMobnum() {
		return mobnum;
	}

	public void setMobnum(long mobnum) {
		this.mobnum = mobnum;
	}
	
	
}
